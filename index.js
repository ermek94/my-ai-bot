require("dotenv").config();
const { Bot } = require("grammy");
const { GoogleGenAI } = require("@google/genai");

const bot = new Bot(process.env.TELEGRAM_TOKEN);
const ai = new GoogleGenAI({ apiKey: process.env.GEMINI_KEY });

// 🧠 Умная инструкция для Ермека без повторения приветствий
const systemInstruction = `
Ты — умный ИИ-ассистент по имени "Ермек" из Казахстана 🇰🇿. 
Твоя цель — бесплатно помогать школьникам, студентам и обычным людям в Казахстане.

Твои суперсилы:
1. Помогаешь с учёбой: пишешь эссе, рефераты, решаешь задачи, объясняешь формулы и темы ЕНТ.
2. Генерируешь тексты: придумываешь красивые поздравления на казахском (с праздниками вроде Наурыз, Айт, День рождения) и русском языках, пишешь стихи, посты для соцсетей.
3. Переводишь тексты идеально между казахским, русским и английским языками.

Твои правила:
- Будь дружелюбным, используй классные смайлики. 
- Всегда отвечай на том языке, на котором к тебе обратился пользователь (если пишут қазақша — отвечай строго на казахском, если на русском — на русском).
- Отвечай чётко, структурировано и красиво.
- Твое имя — Ермек. Называй своё имя и здоровайся ТОЛЬКО если пользователь сам спросил как тебя зовут. В обычном диалоге СРАЗУ отвечай на вопрос по теме, БЕЗ повторения фраз "Привет", "Мен Ермекпін" и "Чем я могу помочь". Не дублируй приветствие!
`;

bot.command("start", (ctx) => {
    ctx.reply("Привет! 🇰🇿 Мен ИИ-көмекші Ермекпін!\n\nЯ твой личный бесплатный искусственный интеллект. Помогаю учиться, решать задачи, писать эссе, рефераты и переводить тексты на казахский, русский и английский! \n\nЗадай мне любой вопрос или попроси написать поздравление 👇");
});

bot.on("message:text", async (ctx) => {
    await ctx.replyWithChatAction("typing");
    try {
        const response = await ai.models.generateContent({
            model: "gemini-2.5-flash",
            contents: ctx.message.text,
            config: { systemInstruction: systemInstruction }
        });
        await ctx.reply(response.text);
    } catch (error) {
        console.error("Ошибка ИИ:", error);
        await ctx.reply("Кешіріңіз, техникалық ақаулар. Извините, технические неполадки. Повторите запрос.");
    }
});

// 🛠️ ХИТРЫЙ ОБХОД ОШИБКИ ПОРТА ДЛЯ БЕСПЛАТНОГО ТАРИФА RENDER
const http = require("http");
const server = http.createServer((req, res) => { res.end("Бот работает!"); });
const port = process.env.PORT || 10000;
server.listen(port, () => { console.log(`Фальшивый веб-порт запущен на порту ${port}`); });

console.log("Ермек успешно запущен в облаке!");
bot.start();
